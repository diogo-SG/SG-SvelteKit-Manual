<h3>Advantages</h3>
<ul>
	<li>Easy to learn</li>
	<li>Fast and lightweight</li>
	<li>Great developer experience</li>
	<li>Great performance</li>
	<li>Great for building web applications</li>
</ul>

<h3>Disadvantages</h3>
<ul>
	<li>Not as popular as React or Vue</li>
	<li>Smaller ecosystem</li>
	<li>Less community support</li>
	<li>Less third-party libraries</li>
</ul>

